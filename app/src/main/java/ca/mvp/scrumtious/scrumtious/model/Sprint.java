package ca.mvp.scrumtious.scrumtious.model;

public class Sprint {

    private String sprintName, sprintDesc;
    private long sprintStartDate, sprintEndDate;

    public String getSprintName() {
        return sprintName;
    }

    public String getSprintDesc() {
        return sprintDesc;
    }

    public long getSprintStartDate() {
        return sprintStartDate;
    }

    public long getSprintEndDate() {
        return sprintEndDate;
    }
}
